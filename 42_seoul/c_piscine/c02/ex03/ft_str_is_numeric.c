#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int	i;
	int	len;

	i = 0;
	len = 0;
	while (str[len] != '\0')
		len++;
	while (i < len)
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
